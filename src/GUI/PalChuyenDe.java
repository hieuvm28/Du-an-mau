    public void loadDanhSach() {
        model = (DefaultTableModel) tbChuyenDe.getModel();
        model.setRowCount(0);
        
        listChuyenDe = dao.select();
        
        for (ChuyenDe cd : listChuyenDe) {
            Object[] row = new Object[]{
                cd.getMaChuyenDe(), cd.getTenChuyenDe(), cd.getThoiLuong(), cd.getHocPhi(), cd.getHinh(), cd.getMoTa()
            };
            model.addRow(row);
        }

        // tbChuyenDe.setRowSelectionInterval(0, 0);
    }
    
    public void addChuyenDe() {
        if (check()) {
            ChuyenDe cd = getModel();
            try {
                dao.insert(cd);
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                loadDanhSach();
                clear();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
                ex.printStackTrace();
            }
        }
        
    }
    
    public void updateChuyenDe() {
        if (check()) {
            ChuyenDe cd1 = getModel();
            try {
                dao.update(cd1);
                JOptionPane.showMessageDialog(this, "Update thành công");
                loadDanhSach();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Update thất bại");
                ex.printStackTrace();
            }
        }
        
    }
    
    public void clear() {
        setModel(new ChuyenDe());
        setStatus(true);
    }
    
    public void edit() {
        try {
            String maCD = tbChuyenDe.getValueAt(index, 0).toString();
            ChuyenDe model = dao.findbyID(maCD);
            setModel(model);
            setStatus(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Lối truy ván dũ liệu");
            ex.printStackTrace();
        }
        
    }
    
    public void deleteChuyenDe() {
        
        try {
            String ma = txtMaChuyenDe.getText();
            dao.delete(ma);
            loadDanhSach();
            clear();
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
            e.printStackTrace();
        }
        
    }
    
    public void showDetail(int index) {
        Model.ChuyenDe cd = listChuyenDe.get(index);
        lblImage.setToolTipText(cd.getHinh());

        //lblImage.setIcon(ShareHelper.readLogo(cd.getHinh()));
        upAvatar(cd.getHinh());
        
        txtMaChuyenDe.setText(cd.getMaChuyenDe());
        txtTenChuyenDe.setText(cd.getTenChuyenDe());
        txtThoiLuong.setText(cd.getThoiLuong() + "");
        txtHocPhi.setText(cd.getHocPhi() + "");
        txtMota.setText(cd.getMoTa());
        lblPresent.setText((index + 1) + " of " + listChuyenDe.size());
        
        tbChuyenDe.setRowSelectionInterval(index, index);
    }
    
