    public boolean check() {
        if (txtMaChuyenDe.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mã chuyên đề không dc để trống");
            txtMaChuyenDe.requestFocus();
            return false;
        }
        if (txtTenChuyenDe.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Tên chyên đề không dc để trống");
            txtTenChuyenDe.requestFocus();
            return false;
        }
        
        if (txtHocPhi.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Học phí không dc để trống");
            txtHocPhi.requestFocus();
            return false;
        } else {
            if ((Double.parseDouble(txtHocPhi.getText())) < 0) {
                JOptionPane.showMessageDialog(this, "Học phí phải lớn hơn 0");
                return false;
            }
        }
        if (txtThoiLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Thời lượng không dc để trống");
            txtHocPhi.requestFocus();
            return false;
        } else {
            if ((Double.parseDouble(txtThoiLuong.getText())) < 0) {
                JOptionPane.showMessageDialog(this, "Thời lượng không dc nhỏ hơn 0");
                return false;
            }
        }
        
        if (lblImage.getToolTipText() == null) {
            JOptionPane.showMessageDialog(this, "Hình ko dc để trống");
            return false;
        }
        
        return true;
    }

